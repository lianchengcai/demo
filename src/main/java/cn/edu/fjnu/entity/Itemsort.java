package cn.edu.fjnu.entity;

public class Itemsort {
	private int id;
	private String itemsortNumber;
	private String itemsortName;
	private String note;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getItemsortNumber() {
		return itemsortNumber;
	}
	public void setItemsortNumber(String itemsortNumber) {
		this.itemsortNumber = itemsortNumber;
	}
	public String getItemsortName() {
		return itemsortName;
	}
	public void setItemsortName(String itemsortName) {
		this.itemsortName = itemsortName;
	}
	public String getNote() {
		return note;
	}
	public void setNote(String note) {
		this.note = note;
	}
	
	
}
