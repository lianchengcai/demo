package cn.edu.fjnu.entity;

public class Suptype {
	private int id;
	private String suptypeNumber;
	private String suptypeName;
	private String note;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getSuptypeNumber() {
		return suptypeNumber;
	}
	public void setSuptypeNumber(String suptypeNumber) {
		this.suptypeNumber = suptypeNumber;
	}
	public String getSuptypeName() {
		return suptypeName;
	}
	public void setSuptypeName(String suptypeName) {
		this.suptypeName = suptypeName;
	}
	public String getNote() {
		return note;
	}
	public void setNote(String note) {
		this.note = note;
	}
	
	
	
}
